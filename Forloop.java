
public class Forloop {

    public static void main(String args[]) {
      
        System.out.print("Using forloop ");
        for(int i = 0; i <= 10; i++)
        {
            System.out.println(i);
        }
        
        System.out.print("Table of 3 using for loop");
        int num = 3;
        for(int i = 1; i<=10;i++)
        {
            System.out.println(num + "x" + i + " = " + num * i);
        }
    }
}
