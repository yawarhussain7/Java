
public class While_loop {

    public static void main(String args[]) {
        
        int counter = 0;
        
        while(counter <= 10)
        {
            System.out.println(counter);
            
            counter++;
        }
        
        System.out.print("Print table of 5 ");
        
        int index = 1;
        int number = 5;
        while(index <= 10)
        {
            System.out.println(number + " x "+ index +" = "+ number * index);
            
            index++;
        }
        
      
        
    }
}
